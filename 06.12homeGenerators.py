
def odd_numbers_in_range(start, end):
    """
    \u0424\u0443\u043d\u043a\u0446\u0456\u044f \u0433\u0435\u043d\u0435\u0440\u0443\u0454 \u043d\u0435\u043f\u0430\u0440\u043d\u0456 \u0447\u0438\u0441\u043b\u0430 \u0432 \u0437\u0430\u0434\u0430\u043d\u043e\u043c\u0443 \u0434\u0456\u0430\u043f\u0430\u0437\u043e\u043d\u0456.

    :param start: \u041f\u043e\u0447\u0430\u0442\u043e\u043a \u0434\u0456\u0430\u043f\u0430\u0437\u043e\u043d\u0443
    :param end: \u041a\u0456\u043d\u0435\u0446\u044c \u0434\u0456\u0430\u043f\u0430\u0437\u043e\u043d\u0443
    :return: \u0413\u0435\u043d\u0435\u0440\u0430\u0442\u043e\u0440 \u043d\u0435\u043f\u0430\u0440\u043d\u0438\u0445 \u0447\u0438\u0441\u0435\u043b
    """
    for number in range(start, end + 1):
        if number % 2 != 0:
            yield number


def values_outside_range(lst, start, end):
    """
    \u0424\u0443\u043d\u043a\u0446\u0456\u044f \u0433\u0435\u043d\u0435\u0440\u0443\u0454 \u0437\u043d\u0430\u0447\u0435\u043d\u043d\u044f \u0437\u0456 \u0441\u043f\u0438\u0441\u043a\u0443, \u0449\u043e \u043d\u0435 \u043f\u0435\u0440\u0435\u0431\u0443\u0432\u0430\u044e\u0442\u044c\u0441\u044f \u0443 \u0434\u0456\u0430\u043f\u0430\u0437\u043e\u043d\u0456 \u043c\u0456\u0436 start \u0456 end.

    :param lst: \u0421\u043f\u0438\u0441\u043e\u043a \u0437\u043d\u0430\u0447\u0435\u043d\u044c
    :param start: \u041f\u043e\u0447\u0430\u0442\u043e\u043a \u0434\u0456\u0430\u043f\u0430\u0437\u043e\u043d\u0443
    :param end: \u041a\u0456\u043d\u0435\u0446\u044c \u0434\u0456\u0430\u043f\u0430\u0437\u043e\u043d\u0443
    :return: \u0413\u0435\u043d\u0435\u0440\u0430\u0442\u043e\u0440 \u0437\u043d\u0430\u0447\u0435\u043d\u044c, \u0449\u043e \u043d\u0435 \u043f\u0435\u0440\u0435\u0431\u0443\u0432\u0430\u044e\u0442\u044c\u0441\u044f \u0443 \u0434\u0456\u0430\u043f\u0430\u0437\u043e\u043d\u0456
    """
    for value in lst:
        if value < start or value > end:
            yield value

